#ตัวดำเนินการทางคณิตศาสตร์ Operator
#ตัวดำเนินการ Operator & ตัวที่ถูกดำเนินการ Operand
# + => Operator
# A+B  > Operand

x = 10
y = 4
print ("ผลบวก" , x+y)
print ("ผลลบ" , x-y)
print("ผลคูณ ", x*y)
print("ผลหาร ", x/y)
print("ผลหารปัดเศษ" , x//y) #ปัดเศษลง
print("ผลรวมยกกำลัง" , 5**3) #ยกกำลัง
print("หารเอาเศษ", x%y) #เอาเศษมาใช้งาน